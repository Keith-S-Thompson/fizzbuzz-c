#include <stdio.h>
int main(void) {
    for (int i = 1; i <= 100; i ++) {
        puts((char[]){
            !(i % 3) * 'F' +
             (i % 3 && i % 5 == 0) * 'B' +
             (i % 3 && i % 5 && i < 10) * (i + '0') +
             (i % 3 && i % 5 && i > 9)  * (i / 10 + '0'),
            !(i % 3) * 'i' +
             (i % 3 && !(i % 5)) * 'u' +
             (i % 3 && i % 5 && i >= 10) * (i % 10 + '0'),
            (!(i % 3) || !(i % 5)) * 'z',
            (!(i % 3) || !(i % 5)) * 'z',
            !(i % 15) * 'B',
            !(i % 15) * 'u',
            !(i % 15) * 'z',
            !(i % 15) * 'z',
            '\0'
        });
    }
}
